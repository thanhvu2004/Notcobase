function normalizeLanguage(value) {
    if (!value) return DEFAULT_LANGUAGE
    const lowered = String(value).toLowerCase()
    return LANGUAGE_ALIAS[lowered] || (messages[lowered] ? lowered : DEFAULT_LANGUAGE)
}

export function getLanguage() {
    return normalizeLanguage(localStorage.getItem(STORAGE_KEY) || DEFAULT_LANGUAGE)
}

export function setLanguage(value) {
    const next = normalizeLanguage(value)
    localStorage.setItem(STORAGE_KEY, next)
    return next
}

function formatString(template, params = {}) {
    return String(template).replace(/{{\s*([^}]+?)\s*}}/g, (_, key) => {
    const normalized = key.trim()
    if (Object.prototype.hasOwnProperty.call(params, normalized)) {
        return params[normalized]
    }
    return `{{${normalized}}}`
    })
}

export function t(key, params = {}) {
    const lang = getLanguage()
    const translation = messages[lang]?.[key] ?? messages[DEFAULT_LANGUAGE]?.[key] ?? key
    return formatString(translation, params)
}

export function supportedLanguages() {
    return Object.keys(messages)
}
